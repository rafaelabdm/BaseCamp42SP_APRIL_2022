int	ft_recursive_power(int nb, int power);

int	ft_recursive_power(int nb, int power)
{
	int	result;

	result = nb;
	if (power == 0)
		return (1);
	if (power < 0 || nb == 0)
		return (0);
	power--;
	result = ft_recursive_power(nb, power) * nb;
	return (result);
}
